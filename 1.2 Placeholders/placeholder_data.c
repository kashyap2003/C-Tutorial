#include <stdio.h>

int main(void)
{
    printf("Hello World\n");// Regular print of a string.

    printf("23\n");         // Regular print of a Number.

    printf("%d\n", 23);     // integer

    printf("%ld\n", 233453635);    // Large integer

    printf("%f\n", 23.45);     // Float

    printf("%lf\n", 232345245.56675);     // Large Float

    printf("%c\n", 'A');     // char

    printf("%s\n", "What's your name");     // String

    printf("%x\n", 23);     // Hex (Hexadecimal)
}